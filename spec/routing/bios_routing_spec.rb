require "rails_helper"

RSpec.describe BiosController, type: :routing do
  describe "routing" do

    it "routes to #index" do
      expect(:get => "/bios").to route_to("bios#index")
    end

    it "routes to #new" do
      expect(:get => "/bios/new").to route_to("bios#new")
    end

    it "routes to #show" do
      expect(:get => "/bios/1").to route_to("bios#show", :id => "1")
    end

    it "routes to #edit" do
      expect(:get => "/bios/1/edit").to route_to("bios#edit", :id => "1")
    end

    it "routes to #create" do
      expect(:post => "/bios").to route_to("bios#create")
    end

    it "routes to #update via PUT" do
      expect(:put => "/bios/1").to route_to("bios#update", :id => "1")
    end

    it "routes to #update via PATCH" do
      expect(:patch => "/bios/1").to route_to("bios#update", :id => "1")
    end

    it "routes to #destroy" do
      expect(:delete => "/bios/1").to route_to("bios#destroy", :id => "1")
    end

  end
end
